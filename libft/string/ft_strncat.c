#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t len)
{
	char	*ss1;

	ss1 = s1;
	while (*ss1++)
		;
	ss1--;
	while (*s2 && len--)
		*ss1++ = *s2++;
	*ss1 = '\0';
	return (s1);
}
