#include "rtv1.h"
#include "colors.h"

void			destr(const int cl, t_clr *c)
{
	*c = (t_clr){(cl / (256 * 256)) % 256, (cl / (256 * 256)) % 256, cl % 256};
}

int				restr(int r, int g, int b)
{
	r > 255 ? r = 255 : 1;
	g > 255 ? g = 255 : 1;
	b > 255 ? b = 255 : 1;
	r < -255 ? r = -255 : 1;
	g < -255 ? g = -255 : 1;
	b < -255 ? b = -255 : 1;
	return ((r * 256 * 256) + (g * 256) + b);
}
